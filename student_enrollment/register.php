<?php
require 'db.php';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $username = $_POST['username'];
    $password = $_POST['password'];
    $first_name = $_POST['first_name'];
    $last_name = $_POST['last_name'];
    $email = $_POST['email'];
    $phone_number = $_POST['phone_number'];
    $address = $_POST['address'];
    $date_of_birth = $_POST['date_of_birth'];

    // Check if username or email already exists
    $stmt = $pdo->prepare("SELECT * FROM students WHERE username = ? OR email = ?");
    $stmt->execute([$username, $email]);
    if ($stmt->fetch()) {
        $error = "Username or email already exists";
    } else {
        $stmt = $pdo->prepare("INSERT INTO students (username, password, first_name, last_name, email, phone_number, address, date_of_birth) VALUES (?, ?, ?, ?, ?, ?, ?, ?)");
        if ($stmt->execute([$username, $password, $first_name, $last_name, $email, $phone_number, $address, $date_of_birth])) {
            header("Location: login.php?registered=1");
            exit();
        } else {
            $error = "Registration failed. Please try again.";
        }
    }
}
